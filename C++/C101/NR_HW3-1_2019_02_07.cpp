// User enters a number. Program determines whether or not the number is between 1 and 100.


#include <iostream>
using namespace std;

int main()
{
  float Number;
  cout << "\nThis program determines whether or not a number is between 1 and 100.\nPlease enter number: ";
  cin >> Number;
  if (Number >= 1 && Number <= 100)
    {
      cout << "Good Input";
    }
  else
    {
      cout << "Bad Input";
    } 
  cout << "\nGoodbye!\n\n";
}


